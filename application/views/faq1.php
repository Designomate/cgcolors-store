<div class="containerFIx-80 max-width900"><h1>What is the difference between theme based design and custom design?</h1>
<p>As the name explains theme based store is the store which is designed using a theme and there is a good chance that the same thing is being used by other people as well. Designers create themes keeping broader business owners in mind so they generalize the various sections on their theme and keep the functionality which many customers may need.
</p><p>
On the other hand, custom store design is something where you hire a designer who designs the site as per your requirements and also based on your specific products. As designer knows what exactly your product and what are your requirements he/she doesn't need to create un-necessary functionality.</p>
